#include "../ft_ssl_md5.h"

void		execute(char *str, char *alg)
{
	int	i;

	i = -1;
	while (++i < 5)
	{
		if (!ft_strcmp(g_algos[i].name, alg))
			g_algos[i].algo(&str);
	}
	free(str);
}
